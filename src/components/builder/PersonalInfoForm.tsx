import React from 'react';
import { Portfolio } from '../../types';

interface Props {
  portfolio: Portfolio;
  onChange: (personalInfo: Portfolio['personalInfo']) => void;
}

export function PersonalInfoForm({ portfolio, onChange }: Props) {
  return (
    <div className="space-y-6">
      <div>
        <label htmlFor="name" className="block text-sm font-medium text-gray-700">
          Full Name
        </label>
        <input
          type="text"
          id="name"
          value={portfolio.personalInfo.name}
          onChange={(e) => onChange({ ...portfolio.personalInfo, name: e.target.value })}
          className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-blue-500 focus:ring-blue-500"
        />
      </div>
      
      <div>
        <label htmlFor="email" className="block text-sm font-medium text-gray-700">
          Email
        </label>
        <input
          type="email"
          id="email"
          value={portfolio.personalInfo.email}
          onChange={(e) => onChange({ ...portfolio.personalInfo, email: e.target.value })}
          className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-blue-500 focus:ring-blue-500"
        />
      </div>

      <div>
        <label htmlFor="bio" className="block text-sm font-medium text-gray-700">
          Bio
        </label>
        <textarea
          id="bio"
          rows={4}
          value={portfolio.personalInfo.bio}
          onChange={(e) => onChange({ ...portfolio.personalInfo, bio: e.target.value })}
          className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-blue-500 focus:ring-blue-500"
        />
      </div>
    </div>
  );
}